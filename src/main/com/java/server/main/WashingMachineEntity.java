package com.java.server.main;

public class WashingMachineEntity {
	
	int wmId;
	String modelNo;
	int serialNo;
	String wmName;
	
	public int getWmId() {
		return wmId;
	}
	public void setWmId(int wmId) {
		this.wmId = wmId;
	}
	public String getModelNo() {
		return modelNo;
	}
	public void setModelNo(String modelNo) {
		this.modelNo = modelNo;
	}
	public int getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(int serialNo) {
		this.serialNo = serialNo;
	}
	public String getWmName() {
		return wmName;
	}
	public void setWmName(String wmName) {
		this.wmName = wmName;
	}
	

}
